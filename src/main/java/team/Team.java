package team;

public class Team {
	
	private int bbsID;
	private String userID;
	private int teamcheck;
	
	public int getBbsID() {
		return bbsID;
	}
	public void setBbsID(int bbsID) {
		this.bbsID = bbsID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public int getTeamcheck() {
		return teamcheck;
	}
	public void setTeamcheck(int teamcheck) {
		this.teamcheck = teamcheck;
	}
}